package group;

public class GroupCate {
	private int CATEID;
	private String CATENAME;
	public int getCATEID() {
		return CATEID;
	}
	public void setCATEID(int cATEID) {
		CATEID = cATEID;
	}
	public String getCATENAME() {
		return CATENAME;
	}
	public void setCATENAME(String cATENAME) {
		CATENAME = cATENAME;
	}
	public GroupCate(int cATEID, String cATENAME) {
		super();
		CATEID = cATEID;
		CATENAME = cATENAME;
	}
	
	
	
	
	
}
